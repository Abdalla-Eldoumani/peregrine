# Agent-infrastructure path fragments that must never appear in a tracked public
# artifact. The README hygiene rule is hard: restate reasoning inline, never
# point a reader at the planning or agent directories.
_INFRA_TOKENS = (".agent", ".planning", ".claude", "CLAUDE.md")


def _read_text(path: str) -> str:
    with open(path, encoding="utf-8") as handle:
        return handle.read()


def readme_hygiene(text: str) -> list:
    """Return the agent-infra tokens found in text (empty list means clean).

    The helper the README hygiene test asserts on: it scans a text blob for any
    .agent / .planning / .claude / CLAUDE.md reference and returns every hit, so
    a published artifact leaks no internal path (rule: no tracked file references
    agent infrastructure).
    """
    return [token for token in _INFRA_TOKENS if token in text]


